    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(check()){
            if(ds.isEmpty()){
                ds.add(h);
                md.addRow(h.toOb());
            } else {
                int c = 0;
                boolean ok =false;
                for(hd i:ds){
                    if(i.giongNhau(h)){
                        JOptionPane.showMessageDialog(this, "Trung thong tin hoa don!");
                        ok =true;
                        break;           
                    }
                    if(i.getK().giongNhau(h.getK())){
                        ++c;
                        if(c>4){
                            JOptionPane.showMessageDialog(this, "So hoa don vuot qua 5!");
                            ok= true;
                            break;
                        }
                    }
                }
                if(!ok){
                    ds.add(h);
                    md.addRow(h.toOb());
                }
            }
            iof.write(f, ds);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

