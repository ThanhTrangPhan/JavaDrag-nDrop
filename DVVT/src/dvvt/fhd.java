    private void prepare() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        String[] cols={"Ma khach hang","Ten khach hang","Ten dich vu","So luong su dung"};
        md = new DefaultTableModel (cols,0);
        b.setModel(md);
        ds= iof.read(f);
        for(hd i: ds){
            md.addRow(i.toOb());
        }
    }

    private boolean check() {
        //throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
        if(makh.getText().isBlank()|| madv.getText().isBlank()|| sl.getText().isBlank()){
           JOptionPane.showMessageDialog(this, "Chua nhap du thong tin!");
           return false;
       }
       List<kh> ds1 = iof.read("KH.txt");
       boolean ok =false;
       h = new hd();
       for(kh i: ds1){
           if(Integer.parseInt(makh.getText()) == i.getMakh()){
               h.setK(i);
               ok =true;
               break;
           }
       }
       if(!ok){
           JOptionPane.showMessageDialog(this, "Khong co thong tin ve khach hang nay!");
           return false;
       }
       ok =false;
       List<dv> ds2 = iof.read("DV.txt");
       
       for(dv i: ds2){
           if(Integer.parseInt(madv.getText()) == i.getMadv()){
               h.setD(i);
               ok =true;
               break;
           }
       }
       if(!ok){
           JOptionPane.showMessageDialog(this, "Khong co thong tin ve dich vu nay!");
           return false;
       }
       h.setSl(Double.parseDouble(sl.getText()));
       return true;
    }
